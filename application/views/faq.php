<b><i style="font-size:22px">Frequently Asked Questions</i></b>
</br></br></br>
<b>Q:</b> Do you sell phones or tablets?
</br></br>
<b>A:</b> NO. While you will see certain areas across our site which lead you to other sites that sell phones and other electronics, WE are here to help you choose a device. You can see features, compare against other devices and get user opinions, but you CANNOT buy from our site.
</br></br>
<b>Q:</b> Can I send you my phone for repairs or upgrades?
</br>
</br>
<b>A:</b> No, we do not upgrade or repair phones/tablets.
</br></br>
<b>Q:</b> Why can't I filter by category X in your Phone Finder?
</br></br>
<b>A:</b> Mobile technology has advanced in great leaps and strides since the inception of our site, particularly in recent years with the advent of smartphones. Many upcoming flagships strive to offer the latest and greatest to capture consumer interest, each outdoing the other with the latest tech wizardry ranging from higher quality displays and processing cores all the way to thickness and connectivity features. 

With the vast quantity of new cutting-edge technology being stuffed into handsets and tablets-not to mention improved software capabilities-it is nearly impossible to filter each of the latest features into distinct categories. Nonetheless, in such cases where we are unable to categorize a particular feature, we always include it as an extra within the phone specifications. These extras can be browsed by using the 'Free text' tool located at the bottom of our Phone Finder. Just type in your keyword and the phone finder will only show results which contain your query, in addition to any other filters you have selected.
</br></br>

<b>Q:</b> I am sure that the information in your catalog for a particular device is not exact. Why is that so?
</br></br>
<b>A:</b> We gather our information mainly from the web sites of manufacturers. Some of them provide very detailed information, others not so much. In the latter case, we sometimes have to resort to other sources which are not as reliable. These include other catalogs, message boards and phone sellers' websites. If you think that there is incorrect data in the catalog for your phone, please sends us an email with the correct data, as well as where you found it, if possible. We thank you in advance.
